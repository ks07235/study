CREATE SCHEMA teamproject COLLATE = utf8_general_ci;
use teamproject;

#회원정보
create table users (
user_id int auto_increment primary key,
id varchar(50) not null unique,
password varchar(200) not null,
name varchar(50) not null,
age int,
mobile varchar(20)
);

#영화
create table movies (
movie_id int auto_increment primary key,
title varchar(200) not null,
price int not null,
movieTime int not null,
genre varchar(50) not null,
poster varchar(300),
synopsis text
# rating decimal(2,1) not null #별점 
);

#영화관
create table cinemas (
cinema_id int auto_increment primary key,
name varchar(100) not null,
location varchar(100) not null,
address varchar(200)
);

#상영 스케쥴
CREATE TABLE schedules (
    schedule_id INT AUTO_INCREMENT PRIMARY KEY,
    movie_id INT NOT NULL,
    cinema_id INT NOT NULL,          -- room_id 대신 cinema_id 직접 참조
    show_date DATE NOT NULL,
    show_time TIME NOT NULL,
    FOREIGN KEY (movie_id) REFERENCES movies(movie_id),
    FOREIGN KEY (cinema_id) REFERENCES cinemas(cinema_id),
    
    -- 같은 영화관에서 같은 날짜, 같은 시간에는 중복 상영 불가
    UNIQUE KEY unique_schedule (cinema_id, show_date, show_time)
);

#좌석
CREATE TABLE seats (
    seat_id INT AUTO_INCREMENT PRIMARY KEY,
    cinema_id INT NOT NULL,          -- room_id 대신 cinema_id 직접 참조
    row_name VARCHAR(10) NOT NULL,
    col_number INT NOT NULL,
    FOREIGN KEY (cinema_id) REFERENCES cinemas(cinema_id)
);

#예매
CREATE TABLE reservations (
    reservation_id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    schedule_id INT NOT NULL,
    seat_id INT NOT NULL,
    reservation_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users(user_id),
    FOREIGN KEY (schedule_id) REFERENCES schedules(schedule_id),
    FOREIGN KEY (seat_id) REFERENCES seats(seat_id),
    
    -- 같은 스케줄의 같은 좌석은 중복 예매 불가
    UNIQUE KEY unique_reservation (schedule_id, seat_id)
);




