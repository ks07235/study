SELECT 
    c.cinema_id,
    c.name as cinema_name,
    c.location,
    COUNT(DISTINCT m.movie_id) as unique_movies,
    GROUP_CONCAT(DISTINCT m.title ORDER BY m.movie_id SEPARATOR ' | ') as movie_list
FROM cinemas c
JOIN schedules s ON c.cinema_id = s.cinema_id
JOIN movies m ON s.movie_id = m.movie_id
GROUP BY c.cinema_id, c.name, c.location
ORDER BY c.cinema_id;